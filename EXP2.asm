 ;LED INTERFACING EXP2

 LDI R16,HIGH(RAMEND)
 OUT SPH,R16
 LDI R16,LOW(RAMEND)
 OUT SPL,R16

 LDI R16,0xFF
 OUT DDRB,R16

 L:
 INC R16
 OUT PORTB,R16
 CALL DELAY
 RJMP L

 DELAY:
 LDI R17,0xF
 AGAIN1:
 LDI R18,0xFF
 AGAIN2:
 LDI R19,0xFF
 AGAIN3:
 NOP
 NOP
 DEC R19
 BRNE AGAIN3
 DEC R18
 BRNE AGAIN2
 DEC R17
 BRNE AGAIN1
 RET

